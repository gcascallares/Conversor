    private void FieldLabel1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_FieldLabel1KeyPressed
        // Si apret√© enter
        if (evt.getKeyChar() == KeyEvent.VK_ENTER) {
            convertirAValor2 = true;
            AplicarConversion();
        }
    }//GEN-LAST:event_FieldLabel1KeyPressed

